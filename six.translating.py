with open('hw2/rna.txt') as file:
    rna_sequence = file.readline()

protein_breakdown = ""
for i in range(0, len(rna_sequence), 3):
    rna_codon = rna_sequence[i] + rna_sequence[i+1] + rna_sequence[i+2]
    aacid = table_of_codon.get(rna_codon)
    if aacid == "stop":
        break
    protein_breakdown += aacid

with open("hw2/rna.txt", "a") as file:
    file.write('\n' + "Белок по трансляции РНК:" + '\n')
    file.write(f'{protein_breakdown}')
